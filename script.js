/* Javascript Assignment
 Create a JavaScript program that does the following:
1. Declare two variables, num1 and num2, and assign them any two numbers.
2. Perform the following operations and store the results in separate variables:
Addition (+)
Subtraction (-)
Multiplication (*)
Division (/)
Modulus (%)
Print the results in a clear format */



// Declare two Variables, num1 and num2
let num1;
let num2;

// Assign them any two numbers
num1 = 15;
num2 = 8;

// Addition Operation (+)
let addResult = num1 + num2;

//Print Result for Addition
console.log(`Addition Result: ${num1} + ${num2} = ${addResult}`);

//Subtraction Operation (-)
let subResult = num1 - num2;

//Print Result for Subtraction
console.log(`Subtraction Result: ${num1} - ${num2} = ${subResult}`);

//Multiplication Operation (*)
let productResult = num1 * num2;

//Print Result for Multiplication
console.log(`Multiplication Result: ${num1} * ${num2} = ${productResult}`);

//Division Operation (/)
let divideResult = num1 / num2;

//Print Result for Division
console.log(`Division Result: ${num1} / ${num2} = ${divideResult}`);

//Modulus Operation (%)
let modulusResult = num1 % num2;

//Print Result for Modulus
console.log(`Modulus Result: ${num1} % ${num2} = ${modulusResult}`);